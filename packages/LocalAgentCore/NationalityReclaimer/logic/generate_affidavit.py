def generate_affidavit(nationality, full_name, birth_location):
    return f"""
    Affidavit of Truth

    I, {full_name}, born on the land known as {birth_location}, hereby declare my lawful nationality as a {nationality}. I reject all presumptions of corporate citizenship, including but not limited to 'U.S. citizen', 'resident', or 'individual'.

    I affirm my standing as a living man/woman on the land, sui juris, and not subject to admiralty jurisdiction.

    This affidavit is made in truth, honor, and authorship.

    Signed,
    {full_name}
    """